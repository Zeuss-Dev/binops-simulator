#include <stdint.h>

uint64_t binary_add(uint64_t num1, uint64_t num2) {
    return num1 + num2;
}

uint64_t binary_subtract(uint64_t num1, uint64_t num2) {
    return num1 - num2;
}

uint64_t binary_multiply(uint64_t num1, uint64_t num2) {
    return num1 * num2;
}

uint64_t binary_divide(uint64_t num1, uint64_t num2) {
    return num1 / num2;
}